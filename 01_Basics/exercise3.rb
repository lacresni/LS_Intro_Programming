# Write a program that uses a hash to store a list of movie titles
# with the year they came out.
# Then use the puts command to make your program print out
# the year of each movie to the screen.
movies = { :starwars => 1975,
           :indiana_jones => 1991,
           :transformers => 2011,
           :batman => 2001,
           :spiderman => 2003 }

puts movies[:starwars]
puts movies[:indiana_jones]
puts movies[:transformers]
puts movies[:batman]
puts movies[:spiderman]
